    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int id = Integer.parseInt(txfId.getText().trim());
        String nombre = txfNombre.getText().trim();
        String telefono = txfTelefono.getText().trim();
        String email = txfEmail.getText().trim();

        if (nombre.isEmpty() || telefono.isEmpty() || email.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "⚠️ Debes rellenar todos los campos antes de insertar un cliente.",
                    "Campos obligatorios",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        controlador.actualizarCliente(id, nombre, telefono, email);

        JOptionPane.showMessageDialog(this, "Cliente insertado Correctamente");
        cargarDatosTabla(controlador.obtenerDatosClientes());

        txfNombre.setText("");
        txfTelefono.setText("");
        txfEmail.setText("");


    }//GEN-LAST:event_btnActualizarActionPerformed

