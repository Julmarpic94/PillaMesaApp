    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        String nombre = txfNombre.getText().trim();
        String telefono = txfTelefono.getText().trim();
        String email = txfEmail.getText().trim();

        if (nombre.isEmpty() || telefono.isEmpty() || email.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "⚠️ Debes rellenar todos los campos antes de insertar un cliente.",
                    "Campos obligatorios",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        controlador.insertarCliente(nombre, telefono, email);

        JOptionPane.showMessageDialog(this, "Cliente insertado Correctamente");
        cargarDatosTabla(controlador.obtenerDatosClientes());

        txfNombre.setText("");
        txfTelefono.setText("");
        txfEmail.setText("");
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int idEliminar = Integer.parseInt(txfId.getText().trim());

        int confirmacion = JOptionPane.showConfirmDialog(this,
                "¿Estás seguro de que deseas eliminar el cliente?",
                "Confirmar eliminación",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);

        if (confirmacion == JOptionPane.YES_OPTION) {
            controlador.eliminarCliente(idEliminar);

            JOptionPane.showMessageDialog(this, "Cliente eliminado Correctamente");

            cargarDatosTabla(controlador.obtenerDatosClientes());
            txfId.setText("");
            txfNombre.setText("");
            txfTelefono.setText("");
            txfEmail.setText("");

        }

    }//GEN-LAST:event_btnEliminarActionPerformed

