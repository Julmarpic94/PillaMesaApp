    private void tablaReservasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaReservasMouseClicked
        System.out.println("Tabla clickeada!");

        int filaSeleccionada = tablaReservas.getSelectedRow();
        if (filaSeleccionada == -1) {
            System.out.println("Ninguna fila seleccionada.");
            return;
        }

        System.out.println("Fila seleccionada: " + filaSeleccionada);

        txfId.setText(tablaReservas.getValueAt(filaSeleccionada, 0).toString());
        txfNombre.setText(tablaReservas.getValueAt(filaSeleccionada, 1).toString());
        txfTelefono.setText(tablaReservas.getValueAt(filaSeleccionada, 2).toString());
        txfEmail.setText(tablaReservas.getValueAt(filaSeleccionada, 3).toString());
    }//GEN-LAST:event_tablaReservasMouseClicked

    public void cargarDatosTabla(Object[][] datos) {
        String[] columnas = {"ID", "Nombre", "Teléfono", "Email"};

        DefaultTableModel modeloTabla = new DefaultTableModel(datos, columnas) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Hace que las celdas no sean editables
            }
        };

        tablaReservas.setModel(modeloTabla);
    }



