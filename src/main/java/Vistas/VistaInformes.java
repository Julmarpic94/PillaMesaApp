    // ✅ Métodos para obtener la ruta de los reportes
    public String obtenerRuta() {
        if (rbtnNormal.isSelected()) {
            return new File("reportes/reporteReservas.jrxml").getAbsolutePath();
        } else if (rbtnGrupos.isSelected()) {
            return new File("reportes/reporteCamarero.jrxml").getAbsolutePath();
        } else if (rbtnGrafico.isSelected()) {
            return new File("reportes/reporteMesas.jrxml").getAbsolutePath();
        } else {
            System.out.println("❌ ERROR: No se seleccionó un tipo de reporte.");
            return null;
        }
    }

