    public void generarInformePDF(String source, String rutaPDF) {
        try {
            conexion = DriverManager.getConnection(DATABASE_URL);
            parametros = new HashMap<>();
            parametros.put("autor", "Julian Martinez");

            File sourceFile = new File(source);
            if (!sourceFile.exists()) {
                System.err.println("❌ ERROR: No se encontró el archivo del reporte " + source);
                return;
            }

            if (compilado) {
                reporte = (JasperReport) JRLoader.loadObjectFromFile(source.replace(".jrxml", ".jasper"));
            } else {
                reporte = JasperCompileManager.compileReport(source);
            }

            JasperPrint informe = JasperFillManager.fillReport(reporte, parametros, conexion);
            JasperViewer.viewReport(informe, false);
            JasperExportManager.exportReportToPdfFile(informe, rutaPDF);

            JOptionPane.showMessageDialog(null, "✅ Reporte PDF generado correctamente");

        } catch (JRException | SQLException ex) {
            ex.printStackTrace();
        }
    }

